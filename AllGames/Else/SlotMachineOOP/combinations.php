<?php

abstract class Combinations {
    public static function threeOnThree(): array {
        return [
            'row1' => [
                [0, 0], [0, 1], [0, 2]
            ],
            'row2' => [
                [1, 0], [1, 1], [1, 2]
            ],
            'row3' => [
                [2, 0], [2, 1], [2, 2]
            ],
            'diag1' => [
                [0, 0], [1, 1], [2, 2]
            ],
            'diag2' => [
                [2, 0], [1, 1], [0, 2]
            ],
        ];
    }

    public static function threeOnFour(): array {
        return [
            'row1' => [
                [0, 0], [0, 1], [0, 2], [0, 3]
            ],
            'row2' => [
                [1, 0], [1, 1], [1, 2], [1, 3]
            ],
            'row3' => [
                [2, 0], [2, 1], [2, 2], [2, 3]
            ],
            'diag1' => [
                [0, 0], [1, 1], [2, 2], [2, 3]
            ],
            'diag2' => [
                [2, 0], [1, 1], [0, 2], [0, 3]
            ],
        ];
    }

    public static function threeOnFive(): array {
        return [
            'row1' => [
                [0, 0], [0, 1], [0, 2], [0, 3], [0, 4]
            ],
            'row2' => [
                [1, 0], [1, 1], [1, 2], [1, 3], [1, 4]
            ],
            'row3' => [
                [2, 0], [2, 1], [2, 2], [2, 3], [2, 4]
            ],
            'diag1' => [
                [0, 0], [1, 1], [2, 2], [2, 3], [2, 4]
            ],
            'diag2' => [
                [2, 0], [1, 1], [0, 2], [0, 3], [0, 4]
            ],
        ];
    }
}
